chrome.extension.getBackgroundPage().setGreenIcon();

function draw_all() {
	var areaDict = chrome.extension.getBackgroundPage().currentAlarmDict;
	var paper = Raphael(10, 50, 300, 600);
	// Creates circle at x = 50, y = 40, with radius 10
	for (var i=0;i<all_towns.length;i++) {
		alertLevel = CheckAlertLevel(all_towns[i].area,areaDict);
		isSelected = alertLevel<=3;
		var radius = isSelected ? 2 : 0.1;
		var circle = paper.circle(all_towns[i].x/100, 800-(all_towns[i].y)/100, radius);		
		//if (alertLevel <=3) 
		//	console.log(all_towns[i].town + " " + alertLevel);
		if (alertLevel == 1) {
			circle.attr("fill", "#f00");
			circle.attr("stroke", "#f00");
		}
		if (alertLevel == 2) {
			circle.attr("fill", "#900");
			circle.attr("stroke", "#900");
		}
		if (alertLevel == 3) {
			circle.attr("fill", "#ff0");
			circle.attr("stroke", "#ff0");
		}
	}
}

var refreshTime = chrome.extension.getBackgroundPage().refreshTime; //5 sec
var alarmValidTime = chrome.extension.getBackgroundPage().alarmValidTime; //1 hour
var alarmCoolDownTime = chrome.extension.getBackgroundPage().alarmCoolDownTime; //30 seconds
var alarmActiveTime = 10*60000; // 10 minutes

function CheckAlertLevel(area,areaDict) {
	var regions = Object.keys(areaDict);
	var areaIndex = regions.indexOf(area);
	if ( areaIndex > -1) {
		var timeLeft = areaDict[area]*refreshTime;
		//console.log("areaIndex:" + areaIndex + ", " + "area:" + area + ", regions[area]:" + regions[area] + ", " + "timeLeft:" + timeLeft); 
		if (timeLeft>alarmValidTime-alarmCoolDownTime) {
			return 1;
		} else if (timeLeft>alarmValidTime-alarmActiveTime){
			return 2;
		} else {
			return 3;
		}
	}
	return 4;
}

